/**
 * if-else Statements:
 * - if: Executes a block of code if a certain condition is true.
 * - else if: Provides an alternative condition to be checked if the previous if statement is false.
 * - else: Specifies a block of code to be executed if none of the preceding conditions are true.
 */

// Conditional Operators

/**
 * Logical Operators:
 * - && (AND): Returns true if both operands are true.
 * - || (OR): Returns true if at least one of the operands is true.
 * - ! (NOT): Inverts the logical state of its operand.
 *
 * Comparison Operators:
 * - == (Equality): Returns true if the operands are equal.
 * - === (Strict Equality): Returns true if the operands are equal in value and type.
 * - > (Greater Than): Returns true if the left operand is greater than the right operand.
 * - < (Less Than): Returns true if the left operand is less than the right operand.
 * - >= (Greater Than or Equal To): Returns true if the left operand is greater than or equal to the right operand.
 * - <= (Less Than or Equal To): Returns true if the left operand is less than or equal to the right operand.
 * - != (Inequality): Returns true if the operands are not equal.
 * - !== (Strict Inequality): Returns true if the operands are not equal in value or type.
 */
